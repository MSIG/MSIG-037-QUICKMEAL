    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
                
        i = 0;
        
        try {
            System.out.println("Bucar: " + txtBuscar.getText());
            lista = acceso.listarIngredientes(txtBuscar.getText());
        } catch (Exception error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrio un error al cargar los datos");
        }
        
        restablecer();
        
        if(lista.isEmpty()){
            mensaje.manipulacionExcepciones("critico", "No existen datos guardados");
        } else {
            //CREAR CAMPOS DE LA TABLA
            tabla.addColumn("Id Ingrediente");
            tabla.addColumn("Id Producto");
            tabla.addColumn("Descripcion Producto");
            tabla.addColumn("Id Item");
            tabla.addColumn("Descripcion Item");
            tabla.addColumn("Proporcion");
            tabla.addColumn("Unidad Medida");
            tabla.setRowCount(lista.size());
            for(ObjetoIngrediente n : lista){
                tabla.setValueAt(n.getId_ingrediente(), i, 0);
                tabla.setValueAt(n.getId_producto(), i, 1);
                tabla.setValueAt(n.getDescripcion_producto(), i, 2);
                tabla.setValueAt(n.getId_item(), i, 3);
                tabla.setValueAt(n.getDescripcion_item(), i, 4);
                tabla.setValueAt(n.getProporcion_ingrediente(), i, 5);
                tabla.setValueAt(n.getUnidad_medida_producto(), i, 6);
                i++;
            }
            tblIngredientes.setModel(tabla);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

