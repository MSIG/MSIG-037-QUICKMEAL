    public void cargarProductos(){
        AccesoProducto accesoProducto = new AccesoProducto();
        ArrayList<ObjetoProducto> listaProductos = new ArrayList();
        listaProductos = accesoProducto.retornaProductos();
        for(ObjetoProducto n : listaProductos){
            cbxProductos.addItem(n.getDescripcion_producto());
        }
    }
    
    public void cargarItems(){
        AccesoItem accesoItem = new AccesoItem();
        ArrayList<ObjetoItem> listaItem = new ArrayList();
        listaItem = accesoItem.listar();
        for(ObjetoItem n : listaItem){
            cbxItems.addItem(n.getDescripcion_item());
        }
    }
    
    public String mostrarEstado(String valor){
        String resultado = "";
        if(valor.equals("Activo")){resultado = "A";}
        if(valor.equals("Inactivo")){resultado = "I";}
        if(valor.equals("A")){resultado = "Activo";}
        if(valor.equals("I")){resultado = "Inactivo";}
        return resultado;
    }

