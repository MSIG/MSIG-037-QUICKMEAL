    public String mostrarEstado(String valor){
        String resultado = "";
        if(valor.equals("Activo")){resultado = "A";}
        if(valor.equals("Inactivo")){resultado = "I";}
        if(valor.equals("A")){resultado = "Activo";}
        if(valor.equals("I")){resultado = "Inactivo";}
        return resultado;
    }

