    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        restablecer();
        
        i = 0;
        
        try {
            if(txtBuscar.getText().isEmpty()){
                lista = acceso.listar();
            } else {
                lista = acceso.buscar(txtBuscar.getText());
            }
        } catch (Exception error) {
            mensaje.manipulacionExcepciones("critico", "Ocurrio un error al cargar los datos");
        }
        
        if(lista.isEmpty()){
            mensaje.manipulacionExcepciones("critico", "No existen datos guardados");
        } else {
            //CREAR CAMPOS DE LA TABLA
            tabla.addColumn("Id Item");
            tabla.addColumn("Id Categoria");
            tabla.addColumn("Descripcion");
            tabla.addColumn("Costo");
            tabla.addColumn("Precio");
            tabla.addColumn("Estado");
            //ASIGNAR NUMERO DE FILAS
            tabla.setRowCount(lista.size());
            //ENVIAR LOS DATOS A CADA FILA
            for(ObjetoItem n : lista){
                tabla.setValueAt(n.getId_item(), i, 0);
                tabla.setValueAt(n.getId_categoria(), i, 1);
                tabla.setValueAt(n.getDescripcion_item(), i, 2);
                tabla.setValueAt(n.getCosto_item(), i, 3);
                tabla.setValueAt(n.getPrecio_item(), i, 4);
                tabla.setValueAt(mostrarEstado(n.getEstado_item()), i, 5);
                i++;
            }
            tblItem.setModel(tabla);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

