    private void btnAgregarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarItemActionPerformed
        
        try {
            
            int x = 0;
            ArrayList<ObjetoDetalleVenta> ventas = new ArrayList();
            ventas = acceso_detalle.buscarIdVenta(txtIdOrden.getText(), txtIdCliente.getText());
            Iterator i = ventas.iterator();
            
            while(i.hasNext()){
               ObjetoDetalleVenta v = new ObjetoDetalleVenta();
               v = (ObjetoDetalleVenta) i.next();
               x = v.getId_venta();
            }
            
            System.out.println("Id Venta: " + x);
            for(int c=0; c<Integer.parseInt(txtCantidadItem.getText()); c++){
                acceso_detalle.insertarDetalleVenta(x, Integer.parseInt(txtIdItem.getText()), 1);
            }
            
            mensaje.manipulacionExcepciones("informacion", "Venta modificada");
        
        } catch(Exception e){
            
        }
       
    }//GEN-LAST:event_btnAgregarItemActionPerformed

