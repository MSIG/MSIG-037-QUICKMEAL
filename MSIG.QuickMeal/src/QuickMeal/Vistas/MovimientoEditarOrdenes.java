    private void mostrarOrdenes(String fecha){
        lista_ordenes = acceso_orden.listarPorFecha(fecha);
        System.err.println(lista_ordenes.size());
        int i = 0;
        for(ObjetoOrden n : lista_ordenes){
            tblOrdenes.setValueAt(n.getId_orden(), i, 0);
            tblOrdenes.setValueAt(n.getId_venta(), i, 1);
            tblOrdenes.setValueAt(n.getId_cliente(), i, 2);
            tblOrdenes.setValueAt(n.getNombre_cliente(), i, 3);
            tblOrdenes.setValueAt(n.getFecha_orden(), i, 4);
            i++;
        }
        tblOrdenes.getColumnModel().getColumn(3).setPreferredWidth(150);
    }
    
    
