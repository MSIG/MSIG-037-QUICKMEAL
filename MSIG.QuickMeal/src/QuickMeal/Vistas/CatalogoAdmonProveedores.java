    private int obtenerIdProveedor(String pNit_proveedor){
        int vId_proveedor = 0;
        ArrayList<ObjetoProveedor> BuscarProveedor = new ArrayList();
        
        try{
            BuscarProveedor = nuevoProveedor.buscarProveedor(pNit_proveedor);
        }catch(Exception error){
            Mensaje.manipulacionExcepciones("critico", "Ocurrio un error al consultar el NIT del proveedor " + error);
        }
        
        if(BuscarProveedor.isEmpty()){
            Mensaje.manipulacionExcepciones("informacion", "Debe crear el proveedor en el sistema");
        }else{
            if(BuscarProveedor.size() > 1){
                System.out.println("EXISTEN " + BuscarProveedor.size() + " PROVEEDORES CON ESTE NUMERO DE NIT");
                Mensaje.manipulacionExcepciones("critico", "Este numero de Nit esta repetido");
            }else{
                vId_proveedor = BuscarProveedor.get(0).getId_proveedor();
            }
        }
        
        return vId_proveedor;
    }
    
    private void buscarEnRegistrosGuardados(){
        String busqueda = txtBuscar.getText();
        
        if(busqueda != null){
            DefaultTableModel tabla = new DefaultTableModel();
            
            ArrayList<ObjetoProveedor> listaProveedores = new ArrayList();
            
            int contadorFilas = 1;
            int cProveedor = 0;
            
            // Verificar algun erro al cargar la informacion de los proveedores
            try{
                listaProveedores = nuevoProveedor.seleccionarProveedor(busqueda);
                
                System.out.println("SE CONSULTARON LOS DATOS DE PROVEEDORES");
                
            }catch(Error error){
                Mensaje.manipulacionExcepciones("critico", "Ocurrio un error al consultar la informacion " + error);
            }
            
            // Mostrar la informacion de los proveedores en la tabla de la vista
            if(listaProveedores.isEmpty()){
                Mensaje.manipulacionExcepciones("critico", "No existe datos almacenados de los proveedores");
            }else{
                tabla.addColumn("NIT");
                tabla.addColumn("NOMBRE");
                tabla.addColumn("DIRECCION");
                tabla.addColumn("CONTACTO");
                tabla.addColumn("TELEFONO");
                tabla.addColumn("DIAS CREDITO");
                
                for(ObjetoProveedor xProveedor : listaProveedores){
                    
                    tabla.setRowCount(contadorFilas);
                    
                    tabla.setValueAt(xProveedor.getNit_proveedor(), cProveedor, 0);
                    tabla.setValueAt(xProveedor.getDescripcion_proveedor(), cProveedor, 1);
                    tabla.setValueAt(xProveedor.getDireccion_proveedor(), cProveedor, 2);
                    tabla.setValueAt(xProveedor.getContacto_proveedor(), cProveedor, 3);
                    tabla.setValueAt(xProveedor.getTelefono_proveedor(), cProveedor, 4);
                    tabla.setValueAt(xProveedor.getDias_credito_proveedor(), cProveedor, 5);
                    
                    System.out.println("Se encontro el registro " + xProveedor.getDescripcion_proveedor());
                    cProveedor++;
                    contadorFilas++;
                    
                }
                
                tblProveedor.setModel(tabla);    
            }
            
        }else{
            mostrarRegistrosGuardados();
        }
        
    }
    
