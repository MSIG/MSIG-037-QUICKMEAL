     private void buscarProveedor() {

        boolean proveedor_encontrado = false;

        ArrayList<ObjetoProveedor> BuscarProveedor = new ArrayList();

        try {
            BuscarProveedor = nuevoProveedor.buscarProveedor(txtNitProveedor.getText());
        } catch (Exception error) {
            Mensaje.manipulacionExcepciones("critico", "Ocurrio un erro al buscar informacion sobre este proveedor " + error);
        }

        if (BuscarProveedor.isEmpty()) {
            proveedor_encontrado = false;
        } else {
            if(BuscarProveedor.size() > 1){
                System.out.println("EXISTEN " + BuscarProveedor.size() + " PROVEEDORES CON ESTE NUMERO DE NIT");
                Mensaje.manipulacionExcepciones("critico", "Este codigo esta repetido");
            }else{
                proveedor_encontrado = true;
                txtDescripcionProveedor.setText(BuscarProveedor.get(0).getDescripcion_proveedor());
                txtTelProveedor.setText(String.valueOf(BuscarProveedor.get(0).getTelefono_proveedor()));
                txtDireccionProveedor.setText(BuscarProveedor.get(0).getDireccion_proveedor());
                txtContactoProveedor.setText(BuscarProveedor.get(0).getContacto_proveedor());
                txtDiasCreditoProveedor.setText(String.valueOf(BuscarProveedor.get(0).getDias_credito_proveedor()));
            }
            
        }
    }
     
    
