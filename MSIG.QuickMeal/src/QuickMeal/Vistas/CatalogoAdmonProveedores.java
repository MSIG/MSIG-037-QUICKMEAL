    private void guardarRegistro(){
        
        // Si se ingresan todos los datos del proveedor asigno valor a los objetos
        if(verificarDatosProveedor()){
            proveedor.setNit_proveedor(txtNitProveedor.getText());
            proveedor.setDescripcion_proveedor(txtDescripcionProveedor.getText());
            proveedor.setTelefono_proveedor(Integer.parseInt(txtTelProveedor.getText()));
            proveedor.setDireccion_proveedor(txtDireccionProveedor.getText());
            proveedor.setContacto_proveedor(txtContactoProveedor.getText());
        }
        
        try{
            nuevoProveedor.insertarProveedor(proveedor);
        }catch(Exception error){
            Mensaje.manipulacionExcepciones("critico", "Ocurrio un error al guardar los datos del proveedor " + error);
        }
        
    }
    
    private boolean verificarDatosProveedor(){
        boolean vDatosCorrectos = true;
        
        if(txtNitProveedor.getText().isEmpty()){
            Mensaje.manipulacionExcepciones("critico", "Ingrese el numero de NIT");
            vDatosCorrectos = false;
        }
        if (txtDescripcionProveedor.getText().isEmpty()) {
            Mensaje.manipulacionExcepciones("critico", "Ingrese la descripcion del proveedor");
            vDatosCorrectos = false;
        }
        if (txtTelProveedor.getText().isEmpty()) {
            Mensaje.manipulacionExcepciones("critico", "Ingrese el numero de Telefono");
            vDatosCorrectos = false;
        }
        if (txtDireccionProveedor.getText().isEmpty()) {
            Mensaje.manipulacionExcepciones("critico", "Ingrese la Direccion del proveedor");
            vDatosCorrectos = false;
        }
        if (txtContactoProveedor.getText().isEmpty()) {
            Mensaje.manipulacionExcepciones("critico", "Ingrese el contacto del proveedor");
            vDatosCorrectos = false;
        }
        
        return vDatosCorrectos;
    }
    
