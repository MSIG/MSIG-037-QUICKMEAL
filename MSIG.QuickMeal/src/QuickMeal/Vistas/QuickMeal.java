    private void verificarProductos(){
        try {
            //verificar si hay productos al minimo
            AccesoProducto acceso_producto = new AccesoProducto();
            if (acceso_producto.buscarProductosEnMinimo().equals("SI")) {
                txtMensaje.setText("Verifique los productos al minimo");
                QuickMeal.btnProductosMinimo.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println("Error al verificar productos al iniciar el programa");
        }
    }
    
