    private void btnProductosMinimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductosMinimoActionPerformed
        try{
            Conexion acceso = new Conexion();
            URL url_reporte = this.getClass().getResource("/QuickMeal/Reportes/PuntoDeReOrden.jasper");
            JasperReport reporte = (JasperReport) JRLoader.loadObject(url_reporte);
            HashMap parametro = new HashMap();
            JasperPrint pantalla = JasperFillManager.fillReport(reporte, parametro, acceso.conectar());
            JasperViewer visualizador = new JasperViewer(pantalla, false);
            visualizador.show();
        }catch(Exception error){
        }
    }//GEN-LAST:event_btnProductosMinimoActionPerformed

